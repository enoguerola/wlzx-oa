package system.wlims.basic.vo;

public class UserAddressVo {
	private String userID;
	private String userAccount;
	private String mainDepartmentName;
	private String mainRoleName;
	private String userName;
	private String email;
	private String officePhone;
	private String mobilePhone;
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUserAccount() {
		return userAccount;
	}
	public void setUserAccount(String userAccount) {
		this.userAccount = userAccount;
	}
	public String getMainDepartmentName() {
		return mainDepartmentName;
	}
	public void setMainDepartmentName(String mainDepartmentName) {
		this.mainDepartmentName = mainDepartmentName;
	}
	public String getMainRoleName() {
		return mainRoleName;
	}
	public void setMainRoleName(String mainRoleName) {
		this.mainRoleName = mainRoleName;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getOfficePhone() {
		return officePhone;
	}
	public void setOfficePhone(String officePhone) {
		this.officePhone = officePhone;
	}
	public String getMobilePhone() {
		return mobilePhone;
	}
	public void setMobilePhone(String mobilePhone) {
		this.mobilePhone = mobilePhone;
	}
	
}
